/// The colors of all byte values.
pub const BYTE_COLORS: [Color32; 256] = {
    let mut out = [Color32::BLACK; 256];

    let ascii_nonprintable = generate_color_gradient::<31>(&[
        (Color32::from_rgb(255, 0, 255), 0.0),
        (Color32::from_rgb(128, 0, 255), 1.0),
    ]);
    let ascii_printable = generate_color_gradient::<95>(&[
        (Color32::from_rgb(0, 128, 255), 0.0),
        (Color32::from_rgb(0, 255, 128), 1.0),
    ]);
    let nonascii = generate_color_gradient::<128>(&[
        (Color32::from_rgb(255, 0, 0), 0.0),
        (Color32::from_rgb(255, 255, 0), 0.66),
        (Color32::from_rgb(255, 255, 255), 1.0),
    ]);

    let mut i = 0;
    while i < 256 {
        let color = match i {
            0 => Color32::from_rgb(100, 100, 100),
            1..=31 => ascii_nonprintable[i - 1],
            32..=126 => ascii_printable[i - 32],
            127 => Color32::from_rgb(64, 128, 0),
            128.. => nonascii[i - 128],
        };

        out[i] = color;

        i += 1;
    }

    out
};

/// Generates a gradient with the given stops
const fn generate_color_gradient<const N: usize>(stops: &[(Color32, f64)]) -> [Color32; N] {
    let mut out = [Color32::BLACK; N];

    assert!(stops.len() >= 2, "need at least two stops for the gradient");

    let mut byte = 0;
    while byte < N {
        let relative_byte = byte as f64 / N as f64;

        let mut i = 1;
        while i < stops.len() && stops[i].1 < relative_byte {
            i += 1;
        }
        if i >= stops.len() {
            i = stops.len() - 1;
        }
        let prev_stop = stops[i - 1];
        let stop = stops[i];
        let diff = stop.1 - prev_stop.1;
        let t = (relative_byte - prev_stop.1) / diff;

        let color = lerp(prev_stop.0, stop.0, t);

        out[byte] = color;

        byte += 1;
    }

    out
}

/// Linearly interpolates between the two given colors.
///
/// If `between` is `0.0`, the result will be `color1` and if it is `1.0`, the result will be
/// `color2`.
pub const fn lerp(color1: Color32, color2: Color32, mut between: f64) -> Color32 {
    between = if between.is_nan() {
        0.5
    } else {
        between.clamp(0.0, 1.0)
    };

    const fn transform(val1: u8, val2: u8, between: f64) -> u8 {
        (val1 as f64 * (1.0 - between) + val2 as f64 * between) as u8
    }

    Color32::from_rgba_premultiplied(
        transform(color1.r(), color2.r(), between),
        transform(color1.g(), color2.g(), between),
        transform(color1.b(), color2.b(), between),
        transform(color1.a(), color2.a(), between),
    )
}

/// The linear opposite color.
///
/// Takes the linear opposite of all color components.
pub const fn linear_opposite(color: Color32) -> Color32 {
    Color32::from_rgba_premultiplied(255 - color.r(), 255 - color.g(), 255 - color.b(), color.a())
}

/// Represents the different possible color maps.
pub enum ColorMap {
    /// The magma color map.
    Magma,
    /// The inferno color map.
    Inferno,
    /// The plasma color map.
    Plasma,
    /// The viridis color map.
    Viridis,
    /// The cividis color map.
    Cividis,
    /// The turbo color map.
    Turbo,
    /// The managua color map.
    Managua,
}

impl ColorMap {
    /// Returns the corresponding color map.
    pub fn get_map(&self) -> &[Color32; 256] {
        match self {
            ColorMap::Magma => &MAGMA,
            ColorMap::Inferno => &INFERNO,
            ColorMap::Plasma => &PLASMA,
            ColorMap::Viridis => &VIRIDIS,
            ColorMap::Cividis => &CIVIDIS,
            ColorMap::Turbo => &TURBO,
            ColorMap::Managua => &MANAGUA,
        }
    }
}

